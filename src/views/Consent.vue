<p>
Introduction:
You are being invited to participate in a clinical trial to evaluate the safety and efficacy of
allogenic exosomes derived from mesenchymal stem cells in reducing epigenetic age. Please
read this form carefully and take your time to understand the information provided. You are free
to ask any questions about the trial, and you may choose to participate or decline to participate.
<p>
Purpose of the Study:
The purpose of this study is to evaluate the safety and efficacy of allogenic exosomes in
reducing epigenetic age in healthy adults.
<p>
Study Procedures:
If you decide to participate, you will be randomized to receive either allogenic exosomes or
placebo. You will receive weekly intravenous infusions of the assigned treatment for 12 weeks.
The dose of allogenic exosomes will be 100 million exosomes per infusion. Participants in the
control group will receive weekly intravenous infusions of placebo (saline solution) for 12 weeks.
You will be required to provide blood samples at baseline, week 6, and week 12 to evaluate
changesin epigenetic age and other biomarkers. You will also be required to undergo physical
exams and complete questionnaires about your health and quality of life.

<p>
Potential Risks:
The risks associated with participation in this study include the following:
● Allergic reactions to the exosomes or the infusion procedure
● Infection at the site of infusion
● Bleeding or bruising at the site of blood draws
Other risks may be discovered as the study progresses.
<p>
Potential Benefits:
The potential benefits associated with participation in this study include the following:
● Reduction in epigenetic age, which may lead to improved health and increased lifespan
● Possible reduction in inflammation, oxidative stress, and cellular senescence
<p>
Confidentiality:
Your personal information will be kept confidential and will only be accessible to authorized
study staff. Your information will be assigned a unique study ID number to protect your privacy.

<p>
Voluntary Participation:
