<p>
1. Title: A randomized controlled trial of the effects of allogenic exosomes on reducing
epigenetic age.
</p>
<p>
2. Background and Rationale: Recent studies have suggested that exosomes, small
extracellular vesicles that contain a variety of biomolecules, may have anti-aging effects.
Exosomes derived from mesenchymal stem cells have been shown to improve tissue repair
and reduce inflammation in preclinical studies, and may also have effects on aging-related
epigenetic changes. However, more research is needed to determine the safety and efficacy
of allogenic exosomes in humans.
<p>
3. Objectives and Hypotheses: The primary objective of this study is to evaluate the safety and
efficacy of allogenic exosomes in reducing epigenetic age in healthy adults. The primary
hypothesis is that treatment with allogenic exosomes will result in a significant reduction in
epigenetic age as measured by the Horvath clock.
<p>

4. Study Design: This will be a randomized, double-blind, placebo-controlled trial. Participants
will be randomized in a 1:1 ratio to receive either allogenic exosomes or placebo. The study
will be conducted at a single site.

<p>
5. Study Population: The study population will include healthy adults aged 50-70 years who
have a Horvath clock epigenetic age of at least 60 years. Participants will be excluded if they
have any medical conditions that could interfere with the study or if they have received any
stem cell or exosome therapies within the past 6 months.

<p>

6. Intervention: Participants in the treatment group will receive weekly intravenous infusions of
allogenic exosomes derived from mesenchymal stem cells for 12 weeks. The dose will be
100 million exosomes per infusion. Participants in the control group will receive weekly
intravenous infusions of placebo (saline solution) for 12 weeks.

<p>
7. Outcome Measures: The primary outcome measure will be changes in epigenetic age as
measured by the Horvath clock. Secondary outcome measures will include changes in
biomarkers of inflammation, oxidative stress, and cellular senescence.

<p>
8. Statistical Analysis: The primary analysis will be conducted using a two-sample t-test to
compare the change in epigenetic age between the treatment and control groups. A
pre-specified subgroup analysis will be conducted to evaluate whether treatment effects vary
by baseline epigenetic age. A secondary analysis will be conducted to evaluate changes in
biomarkers of inflammation, oxidative stress, and cellular senescence.

<p>
9. Data Monitoring and Safety: An independent data and safety monitoring board (DSMB) will
be established to monitor the safety of the study participants. Adverse events will be
monitored and reported to the DSMB and the institutional review board.

<p>
10. Ethical Considerations: The study will be conducted in accordance with the Declaration of
Helsinki and the International Conference on Harmonisation Good Clinical Practice
guidelines. All participants will provide written informed consent prior to enrollment in the
study.

